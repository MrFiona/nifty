from __future__ import absolute_import


def distanceOnEarth(lat1, lon1, lat2, lon2):
    """
    Calculates great circle distance [in km] between two points on Earth 
    specified in decimal degrees as longitude/latitude. Uses haversine algorithm.
    """
    from math import sin, cos, asin, radians, sqrt
    
    # convert decimal degrees to radians 
    lon1, lat1, lon2, lat2 = map(radians, [lon1, lat1, lon2, lat2])
    
    # haversine formula 
    dlon = lon2 - lon1 
    dlat = lat2 - lat1 
    a = sin(dlat/2)**2 + cos(lat1) * cos(lat2) * sin(dlon/2)**2
    c = 2 * asin(sqrt(a)) 
    km = 6367 * c
    return km 
